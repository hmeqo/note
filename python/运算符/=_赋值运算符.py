"""= -= +=  赋值运算符"""


'''
1.赋值运算符有 = -= +=

=  将右侧的值赋值给左侧
-=  左右两侧的值都是数字时有效，将右侧的值以减的形式赋值给左侧
+=  左右两侧的值都是数字时有效，将右侧的值以加的形式赋值给左侧
'''

print('1.')
print('——————————————————————————————————————————————————')

a = 10
b = 20
print('1.', '赋值前的变量a', a)
print('赋值前的变量b', b)
a = b
print('将b赋值给a后的变量a', a)
a = 10
b = 20
a -= b
print('2.', '将b以减的形式赋值给a后的变量a', a)
a = 10
b = 20
a += b
print('3.', '将b以加的形式赋值给a后的变量a', a)

print('——————————————————————————————————————————————————')
