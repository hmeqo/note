"""del  删除对象的语句"""


'''
1.可以删除一个对象

减少一次引用，没有引用时，释放内存
对于列表支持下标索引指定删除元素，字典支持填写键指定删除键值对
'''

print('1.')
print('——————————————————————————————————————————————————')

a = (1, 2, 3)
print('1.', '删除 a 这个元组')
del a
a = [1, 2, 3]
print('2.', '删除 a 这个列表中索引为1的元素，删除前', a)
del a[1]
print('删除后', a)
a = {'我是': '字典', '键': '值'}
print('3.', '删除 a 这个字典中键为 \'键\' 的键值对，删除前', a)
del a['键']
print('删除后', a)

print('——————————————————————————————————————————————————')
