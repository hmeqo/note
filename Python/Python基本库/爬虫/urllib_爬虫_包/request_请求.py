'''
5.build_opener() install_opener()

开启者

build_opener()  创建 opener，临时的
    参数:
        handlers: 管理者，ProxyHandle() 创建管理者
    返回的对象的可使用方法属性:
        open()  打开网址，开爬
            参数:
                url: 网址
        addheaders  添加标头，一个元组元素的列表的变量
            赋值:
                [('User-Agent', 'hander')]
                元组中第二个变量就是要添加的标头
install_opener()  安装 opener，之后可用普通的方式打开
    修改变量的形式安装，不是永久安装到系统
    参数:
        opener: 开启者
'''

