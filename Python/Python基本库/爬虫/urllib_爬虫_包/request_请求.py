'''
3.Request()

整合

参数:
    url: 网址
    data=None: application 格式的数据
    headers={}: 标头，传入一个字典

返回该类对象，可作为 urlopen() 的 url 参数
返回的对象的可使用方法属性:
    headers  一个字典，返回 headers
    add_header()  添加 header
        参数:
            key: 键
            val: 值
'''

