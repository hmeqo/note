"""range()  内置_BIF"""


'''
1.产生一个整数序列

产生一个从a（参数一）到b（参数二）的整数序列，可以指定步长（参数三）
起始默认从0开始，步长默认1
与其它代码配合使用
'''
print('1.')
print('——————————————————————————————————————————————————')

print('1.', [i for i in range(10)])
print('2.', [i for i in range(0, 10, 1)])
print('3.', [i for i in range(9, -1, -1)])
print('4.', [i for i in range(0, -10, -1)])
print('5.', list(range(10)))

print('——————————————————————————————————————————————————')
print('\n')
