"""什么是序列"""


'''
1.序列分为不可变序列和可变序列

不可变序列有：字符串，元组
可变序列有：列表，字典，集合

序列是一个可迭代的对象
（内置_BIF range() 产生的对象也是一个可迭代对象，一个序列）
'''

print('1.')
print('——————————————————————————————————————————————————')

print('1.', '不可变序列')
print('字符串')
print(('元', '组'))
print('2.', '可变序列')
print(['列', '表'])
print({'字': '典'})
print({'集', '合'})

print('——————————————————————————————————————————————————')
print('\n')
